package com.oa.entity;

/**
 * 图片坐标实体类
 * 
 * @author Administrator
 * 
 */
public class OaCoord {

	private int x;// x坐标值

	private int x1;// x1坐标值
	
	private int y;// y坐标值
	
	private int y1;// y1坐标值
	
	private int w;// 图片宽度

	public int getX() {
		return x;
	}

	public void setX(int x) {
		this.x = x;
	}

	public int getX1() {
		return x1;
	}

	public void setX1(int x1) {
		this.x1 = x1;
	}

	public int getY() {
		return y;
	}

	public void setY(int y) {
		this.y = y;
	}

	public int getY1() {
		return y1;
	}

	public void setY1(int y1) {
		this.y1 = y1;
	}

	public int getW() {
		return w;
	}

	public void setW(int w) {
		this.w = w;
	}

	public int getH() {
		return h;
	}

	public void setH(int h) {
		this.h = h;
	}

	private int h;// 图片高度
}
